package it.xbladecopter.comm.serial.commands;

import it.xbladecopter.comm.gui.ConnectionDialog;

import org.eclipse.core.commands.AbstractHandler;
import org.eclipse.core.commands.ExecutionEvent;
import org.eclipse.core.commands.ExecutionException;
import org.eclipse.swt.widgets.Display;

public class ParametersHandler extends AbstractHandler {

	@Override
	public Object execute(ExecutionEvent event) throws ExecutionException {
		ConnectionDialog dialog = new ConnectionDialog(Display.getCurrent()
				.getActiveShell());

		dialog.open();
		return null;
	}
}
